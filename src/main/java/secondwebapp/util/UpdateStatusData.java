package secondwebapp.util;

public class UpdateStatusData {

	public String username;
	public String usernameToChange;
	public String status;

	
	public UpdateStatusData(){
		
	}
	
	public UpdateStatusData(String username, String usernameToChange, String status){
		
		
		this.username = username;
		this.usernameToChange = usernameToChange;
		this.status = status;

	}

	
	
	

}
